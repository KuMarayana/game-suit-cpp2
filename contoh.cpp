#include <iostream>

void FranceNumber() {
     int Number[5] = {2, 3, 6, 9, 15};
     int Number2[5] = {3, 7, 10, 13, 20};

     for (int i = 0; i <= Number; i++) {
         for (int j = 0;j <= Number2; j++) {
             if (i[5] < 12 && j[5] > 13) {
                 cout<<i<<" "<<j<<endl;
             }
         }
     }
}

